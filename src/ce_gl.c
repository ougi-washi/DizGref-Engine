#define INIT_OPENGL_FUNCTION(func, func_type) \
    func = (func_type)glfwGetProcAddress(#func); \
    if (!func) { \
        fprintf(stderr, "Failed to load OpenGL function: %s\n", #func); \
        exit(1); \
    }

